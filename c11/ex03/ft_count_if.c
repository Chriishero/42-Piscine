int	ft_count_if(char **tab, int length, int (*f)(char *))
{
	int	j;
	int	i;

	j = 0;
	i = 0;
	while (i < length && tab[i])
	{
		if ((*f)(tab[i]) != 0)
			j++;
		i++;
	}
	return (j);
}
